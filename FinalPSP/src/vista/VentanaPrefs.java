    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed

        if (somethingChanged()) {
            
            try {
                
                //envio la orden al servidor
                int orden = 0;
                SealedObject so =Seguridad.cifrar(this.clavePubAjena, orden);
                Comunicacion.enviarObjeto(servidor, so);
                
                //envio el usuario
                modUsu();
                so = Seguridad.cifrar(clavePubAjena, this.usu);
                Comunicacion.enviarObjeto(servidor, so);
                
                //recibo el nuevo usuario
                so = (SealedObject) Comunicacion.recibirObjeto(servidor);
                Usuario newU = (Usuario) Seguridad.descifrar(clavePrivPropia, so);
                VentanaUsuario.usu = newU;

                JOptionPane.showMessageDialog(null, "Preferencias actualizadas con éxito.");
                this.dispose();
                
            } catch (NoSuchAlgorithmException | NoSuchPaddingException | InvalidKeyException | IOException | IllegalBlockSizeException | ClassNotFoundException | BadPaddingException ex) {
                Logger.getLogger(VentanaPrefs.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "No has modificado nada.");
        }
        
        
    }//GEN-LAST:event_btnAceptarActionPerformed

