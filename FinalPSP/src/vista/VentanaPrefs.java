    private boolean somethingChanged() {
        boolean change = false;
        
        int th = 0;
        int qh = 0;
        
        if (usu.getT_hijos() == 'S') {
            th = 1;
        }else{
            th = 2;
        }
        if (usu.getQ_hijos() == 'S') {
            qh = 1;
        }else{
            qh = 2;
        }
        
        if (cbInteres.getSelectedIndex() != usu.getInteres() ||
            cbRelacion.getSelectedIndex() != usu.getRelacion() ||
            sldArte.getValue() != usu.getArte() ||
            sldDeporte.getValue() != usu.getDeporte() ||
            sldPolitica.getValue() != usu.getPolitica() ||
            cbTHijos.getSelectedIndex() != th ||
            cbQHijos.getSelectedIndex() != qh)
        {
            change = true;
        }
        return change;
    }

    private void rellenarInfoUsu() {
        cbInteres.setSelectedIndex(usu.getInteres());
        cbRelacion.setSelectedIndex(usu.getRelacion());
        if (usu.getT_hijos() == 'S') {
            cbTHijos.setSelectedIndex(1);
        }else{
            cbTHijos.setSelectedIndex(2);
        }
        if (usu.getQ_hijos() == 'S') {
            cbQHijos.setSelectedIndex(1);
        }else{
            cbQHijos.setSelectedIndex(2);
        }
        sldArte.setValue(usu.getArte());
        sldDeporte.setValue(usu.getDeporte());
        sldPolitica.setValue(usu.getPolitica());
        
    }

    private void modUsu() {
        this.usu.setInteres(cbInteres.getSelectedIndex());
        this.usu.setRelacion(cbRelacion.getSelectedIndex());
        if (cbTHijos.getSelectedIndex() == 1) {
            this.usu.setT_hijos('S');
        }else{
            this.usu.setT_hijos('N');
        }
        if (cbQHijos.getSelectedIndex() == 1) {
            this.usu.setQ_hijos('S');
        }else{
            this.usu.setQ_hijos('N');
        }
        this.usu.setArte(sldArte.getValue());
        this.usu.setDeporte(sldDeporte.getValue());
        this.usu.setPolitica(sldPolitica.getValue());
    }
    
    private void setValuesPrefs() {
        lblArte.setText(sldArte.getValue()+"");
        lblDeporte.setText(sldDeporte.getValue()+"");
        lblPolitica.setText(sldPolitica.getValue()+"");
    }
